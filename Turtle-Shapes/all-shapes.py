def star(size, color):
    turtle.begin_fill()
    turtle.color(color)
    for i in range(5):
        forward(size)
        right(144)
    turtle.end_fill()
star(155, "green")

def circle(size, color):
    turtle.begin_fill()
    turtle.color(color)
    width(size)
    circle(180)
    turtle.end_fill()
circle(132, "yellow")

