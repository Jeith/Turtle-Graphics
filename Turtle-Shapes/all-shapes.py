from turtle import *

def square(size, color):
    turtle.begin_fill()
    turtle.color(color)
    forward(size)
    right(90)
    forward(size)
    right(90)
    forward(size)
    right(90)
    forward(size)
    turtle.end_fill()
square(100, "blue")

